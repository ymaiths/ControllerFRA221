void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if(GPIO_Pin == GPIO_PIN_13)
  {
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5,GPIO_PIN_RESET);
		for(int i = 0; i < 12 ;i++){
			Memo[i] = 0;
		}
		j = 0;
		Check = 1;
  }
}

void ButtonMatrixRead(){
	static uint8_t X = 0;

	for(int i = 0; i< 4; i++)
	{
		if(HAL_GPIO_ReadPin(BMX_L[i].Port, BMX_L[i].Pin) == GPIO_PIN_RESET)
		{
			ButtonState |= 1 << (X+(i*3));
			Memo[j] = ButtonState;
			if (Memo[j] != ID[j]){
					Check &= 0;
				}
			j += 1;

		}
		else
		{
			ButtonState &= ~(1 << (X+(i*3)));
		}
	}

	if (Check == 1 && Memo[11] == 2048){
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5,GPIO_PIN_SET);
		Check = 3;
	}


	HAL_GPIO_WritePin(BMX_R[X].Port,BMX_R[X].Pin,GPIO_PIN_SET);

	uint8_t nextX = (X+1)%3;
	HAL_GPIO_WritePin(BMX_R[nextX].Port,BMX_R[nextX].Pin,GPIO_PIN_RESET);
	X = nextX;
}
